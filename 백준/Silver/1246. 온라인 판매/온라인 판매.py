영양란수,잠재고객수 = tuple(map(int,input().split()))
잠재고객의원하는가격=[]
for i in range(0,잠재고객수):
    잠재고객의원하는가격.append(int(input()))
잠재고객의원하는가격.sort()
가능수익금=[]
가능수익금2=[]
if 잠재고객수>=영양란수 :
    살수있는최대사람수=영양란수
else:
    살수있는최대사람수=잠재고객수
for j in range(잠재고객수-살수있는최대사람수,잠재고객수):
    수익금=잠재고객의원하는가격[j]*(잠재고객수-j)
    가능수익금.append(수익금)
    가능수익금2.append(수익금)
가능수익금.sort(reverse=True)
매매가격=잠재고객의원하는가격[가능수익금2.index(가능수익금[0])+잠재고객수-살수있는최대사람수]
print(매매가격)
print(가능수익금[0])